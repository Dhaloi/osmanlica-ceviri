function converToOttoman(unicode) {

var convertForm = document.getElementById('userNameTxt');

  if (document.selection) {

convertForm.focus();

sel = document.selection.createRange();

sel.text = String.fromCharCode(unicode);

}

else if (document.getElementById('userNameTxt').selectionStart || document.getElementById('userNameTxt').selectionStart == "0") {

 var startPos = document.getElementById('userNameTxt').selectionStart;

var endPos = document.getElementById('userNameTxt').selectionEnd;

 var chaine = document.getElementById('userNameTxt').value;

convertForm.value = chaine.substring(0, startPos) + String.fromCharCode(unicode) + chaine.substring(endPos, chaine.length);

} else {

 convertForm.value += String.fromCharCode(unicode);

}

var obj=document.getElementById('userNameTxt');

obj.focus();

obj.scrollTop=obj.scrollHeight;

 }

